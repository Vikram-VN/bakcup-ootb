/**
 * Checks if a locale code is valid.
 *
 * @param      {string}   localeCode  The locale code in _BCP 47_ format, e.g., 'en-AU'
 * @return     {boolean}  `true` if valid, `false` otherwise.
 */
function validate(localeCode) {
  if (!localeCode || !localeCode.trim() || /[^a-zA-Z-]+/.test(localeCode)) {
    return false;
  }

  const parts = localeCode.split('-');
  const [language, country] = parts;

  let valid = languageCodes.has(language);

  if (country) {
    valid = valid && countryCodes.has(country);
  } else if (parts.length === 2) {
    valid = false;
  }

  return valid;
}

